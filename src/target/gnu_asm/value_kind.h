#ifndef LIBMCB_TARGET_GNU_ASM_VALUE_KIND_H
#define LIBMCB_TARGET_GNU_ASM_VALUE_KIND_H

#define IS_INT(VALUE_KIND) \
	(IS_INT_IMM(VALUE_KIND) || \
	 IS_INT_MEM(VALUE_KIND) || \
	 IS_INT_REG(VALUE_KIND))

#define IS_INT_IMM(VALUE_KIND) \
	((VALUE_KIND) >= I8_IMM_VALUE && \
	 (VALUE_KIND) <= I64_IMM_VALUE)
#define IS_INT_MEM(VALUE_KIND) \
	((VALUE_KIND) >= I8_MEM_VALUE && \
	 (VALUE_KIND) <= I64_MEM_VALUE)
#define IS_INT_REG(VALUE_KIND) \
	((VALUE_KIND) >= I8_REG_VALUE && \
	 (VALUE_KIND) <= I64_REG_VALUE)

#define IS_IMM(VALUE_KIND) \
	(IS_INT_IMM(VALUE_KIND))
#define IS_MEM(VALUE_KIND) \
	(IS_INT_MEM(VALUE_KIND))
#define IS_REG(VALUE_KIND) \
	(IS_INT_REG(VALUE_KIND))

#define CASE_I8_VALUE \
	case I8_IMM_VALUE: \
	case I8_MEM_VALUE: \
	case I8_REG_VALUE
#define CASE_I16_VALUE \
	case I16_IMM_VALUE: \
	case I16_MEM_VALUE: \
	case I16_REG_VALUE
#define CASE_I32_VALUE \
	case I32_IMM_VALUE: \
	case I32_MEM_VALUE: \
	case I32_REG_VALUE
#define CASE_I64_VALUE \
	case I64_IMM_VALUE: \
	case I64_MEM_VALUE: \
	case I64_REG_VALUE

#define CASE_IMM_VALUE \
	case I8_IMM_VALUE: \
	case I16_IMM_VALUE: \
	case I32_IMM_VALUE: \
	case I64_IMM_VALUE
#define CASE_MEM_VALUE \
	case I8_MEM_VALUE: \
	case I16_MEM_VALUE: \
	case I32_MEM_VALUE: \
	case I64_MEM_VALUE
#define CASE_REG_VALUE \
	case I8_REG_VALUE: \
	case I16_REG_VALUE: \
	case I32_REG_VALUE: \
	case I64_REG_VALUE

enum GNU_ASM_VALUE_KIND {
	UNKOWN_VALUE,

	I8_IMM_VALUE,
	I16_IMM_VALUE,
	I32_IMM_VALUE,
	I64_IMM_VALUE,

	I8_MEM_VALUE,
	I16_MEM_VALUE,
	I32_MEM_VALUE,
	I64_MEM_VALUE,

	I8_REG_VALUE,
	I16_REG_VALUE,
	I32_REG_VALUE,
	I64_REG_VALUE
};

#endif
